public class increment_and_decre {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        int y =7;
        System.out.println(y);
        y++;
        System.out.println(y);
       
        //x++= increment x
        //++x=increment then use
    
    }
}
