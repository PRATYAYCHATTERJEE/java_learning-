public class double_datatype {
    public static void main(String[] args) {
        double x= 2;
        double y =3;
        double z=x/y;
        System.out.println(z);

        double a =3;
        double b =4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
